#import "TRLURLQueryParameters.h"

@implementation TRLURLQueryParameters

- (instancetype)initWithField:(id)field value:(id)value {
    self = [super init];
    if (!self) {
        return nil;
    }

    self.field = field;
    self.value = value;

    return self;
}

- (NSString *)URLEncodedStringValue {
    return [NSString stringWithFormat:@"%@=%@", self.field, self.value];
}

@end
